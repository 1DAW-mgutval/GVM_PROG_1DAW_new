package Ej9;

public class Maquinista {
    private String dni;
    private String nombre;
    private double sueldo;
    private String rango;

    public Maquinista (String dni, String nombre, double sueldo, String rango) {
        this.dni = dni;
        this.nombre = nombre;
        this.sueldo = sueldo;
        this.rango = rango;
    }
}
